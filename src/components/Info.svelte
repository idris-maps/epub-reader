  <h2>What?</h2>
  <p><a href="https://en.wikipedia.org/wiki/EPUB">.epub</a> is an e-book format. This app lets you read them in the browser.</p>
  <p>The books will be saved in browser memory. You can bookmark the page to come back later. Clearing the browser cache will delete them.</p>
  <p>No files are sent to remote servers. No usage data collected. The source code is available <a href="https://github.com/idris-maps/epub-reader">here</a>.</p>
  <p>A selection of sites where you can find books in the .epub format:</p>
  <ul>
