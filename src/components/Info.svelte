  <h2>What?</h2>
  <p><a href="https://en.wikipedia.org/wiki/EPUB">.epub</a> is the most common e-book format. Here you can read your books online.</p>
  <p>The books will be saved in browser memory. You can bookmark the page to come back later. Clearing the browser cache will delete them. No files are sent to remote servers.</p>
  <p>We do not do not collect any usage data. The source code is available <a href="https://github.com/idris-maps/epub-reader">here</a>.</p>
